function dp(name, pop, civ) {
    const r = REGIONS[name];
    if (!r) { console.warn("Missing region: " + name); return null; }
    return { name, lat: r.lat, lng: r.lng, population: pop, civilization: civ || name };
}

function mkEra(year, era, entries) {
    const regions = entries.map(e => dp(e[0], e[1], e[2])).filter(Boolean);
    let label;
    if (year < 0) label = Math.abs(year) + " BC";
    else if (year === 0) label = "1 AD";
    else label = year + " AD";
    return { year, label, era, regions };
}

